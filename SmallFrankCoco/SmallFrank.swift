import Foundation

public final class SmallFrank {
    let name = "SmallFrank"
    
    public init() {}
    
    public func add(a: Int, b: Int) -> Int {
        return a + b
    }
    
    public func sub(a: Int, b: Int) -> Int {
        return a - b
    }
    
    public func times(a: Int, b: Int) -> Int {
        return a * b
    }
    
}
